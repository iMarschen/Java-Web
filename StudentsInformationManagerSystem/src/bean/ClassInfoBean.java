package bean;

public class ClassInfoBean {  
    
    /** 
     * 属性和数据库中的字段相对应 
     */  
      
    private Integer cid;  
      
    private String cname;  
      
    public ClassInfoBean() {  
          
    }  
  
    public Integer getCid() {  
        return cid;  
    }  
  
    public void setCid(Integer cid) {  
        this.cid = cid;  
    }  
  
    public String getCname() {  
        return cname;  
    }  
  
    public void setCname(String cname) {  
        this.cname = cname;  
    }  
}  
